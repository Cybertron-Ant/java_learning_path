class HelloWorl {
  public static void main(String[] args) {
    /* [] is used to initialize an array, [5] sets the lenght of the array to 5 - 20 bytes */
    int[] a = new int[5];

    /* a[0] sets the first index position of the array to the value 10 */
    a[0] = 10;

    /* a[1] sets the second index position of the array to the value 20 */
    a[1] = 20;

    /* a[2] sets the third index position of the array to the value 100 */
    a[2] = 100;

    /* a[3] sets the fourth index position of the array to the value 5000 */
    a[3] = 5000;
    
  }// end method
}// end class

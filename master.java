class HelloWorld {
  public static void main(String[] args) {
    
    /*--age means "first print then decrease the value" while age-- means "first decrease the value then print"*/
    int age = 11;
    System.out.println(--age);
    System.out.println(age--);
    System.out.println(age);
    System.out.println(--age);
    System.out.println(age);
    
  }// end method
}// end class
class HelloWorld {
  public static void main(String[] args) {
    
    /* ++age means "first print then increase the value" while age++ means "first increase the value then print" */
    int age = 11;
    System.out.println(++age);
    System.out.println(age++);
    System.out.println(++age);
    System.out.println(age++);
    System.out.println(++age);
    
  }// end method
}// end class